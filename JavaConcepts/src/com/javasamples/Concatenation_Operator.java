package com.javasamples;

public class Concatenation_Operator {

	public static void main(String[] args) {
    
		String a="Mo";
		String b="Jo";
		int x = 10;
		int y = 20;
		
		System.out.println(a+b);
		System.out.println(x+y);
		System.out.println(a+b+x+y);
		System.out.println(x+y+a+b);
		System.out.println(a+b+(x+y));
		

	}

}
